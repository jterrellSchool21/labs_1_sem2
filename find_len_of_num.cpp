#include "libft.h"

size_t		find_len_of_num(long long int n)
{
	int len;

	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = -n;
		len++;
	}
	if (n > 0 && n < 10)
		return (len + 1);
	while (n != 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
