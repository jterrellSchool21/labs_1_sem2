#include "libft.h"

void	ft_foreach(int *tab, int length, void (*f)(int))
{
	int i;

	if (tab && f)
	{
		i = 0;
		if (length > 0)
		{
			while (length != 0)
			{
				(*f)(*tab);
				length--;
				tab++;
			}
		}
	}
}
