#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		res;
	size_t	len;

	res = 0;
	if (s1 && s2)
	{
		len = ft_strlen((char *)s1);
		while (*s1 == *s2 && *s1 != '\0' && *s2 != '\0')
		{
			len--;
			s1++;
			s2++;
		}
		if (*s1 == '\0' && *s2 == '\0' && len == 0)
			res = 1;
	}
	return (res);
}
