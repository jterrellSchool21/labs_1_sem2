int		ft_count_if(char **tab, int (*f)(char *))
{
	int i;

	i = 0;
	if (tab && f)
	{
		while (**tab != '\0')
		{
			if ((*f)(*tab) == 1)
				i++;
			tab++;
		}
	}
	return (i);
}
